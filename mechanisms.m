Module[{ markedForDeletion },

	If[ MatchQ[head, Head[deletionPattern]  ],

		markedForDeletion = Position[ indices,  deletionPattern[[1]] ];
		head[ Extract[indices, markedForDeletion], SparseArray[Extract[ #, markedForDeletion ]]& /@ data ],


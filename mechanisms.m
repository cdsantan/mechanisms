butterflyDecomposition[ o_origami ] := Module[
{ results = FixedPointList[ butterflyDecompositionStep, {{},o} ], remainingOrigami },
	remainingOrigami = Last[results][[2]];

	{ Drop[ Drop[ results[[All,1]], -1],1], remainingOrigami }
